    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        boolean check = true;
        DefaultTableModel mr = (DefaultTableModel) jt_introR.getModel();
        String regi = "";

        for (int i = 0; i < m.getLista().size(); i++) {
            if (i == m.getLista().size() - 1) {
                if (mr.getValueAt(0, i) == null || mr.getValueAt(0, i).equals("")) {
                    JOptionPane.showMessageDialog(this, "El campo de " + m.getLista().get(i).getNombre() + "esta vacio");
                    check = false;

                } else if (m.getLista().get(i).getLongitud() < ((String) mr.getValueAt(0, i)).length() || mr.getValueAt(0, i) == null) {
                    JOptionPane.showMessageDialog(this, "La longitud de " + m.getLista().get(i).getNombre() + " excede");
                    check = false;
                } else {
                    regi += mr.getValueAt(0, i) + "\n";
                }
            } else {
                if (mr.getValueAt(0, i) == null || mr.getValueAt(0, i).equals("")) {
                    JOptionPane.showMessageDialog(this, "El campo de " + m.getLista().get(i).getNombre() + "esta vacio");
                    check = false;

                } else if (m.getLista().get(i).getLongitud() < ((String) mr.getValueAt(0, i)).length() || mr.getValueAt(0, i) == null) {
                    JOptionPane.showMessageDialog(this, "La longitud de " + m.getLista().get(i).getNombre() + " excede");
                    check = false;
                } else {
                    regi += mr.getValueAt(0, i) + "|";
                }

            }
        }
        //insertar al arbol

        if (arbol.getRoot() == null) {
            BTreeNode nuevo = new BTreeNode(arbol.getT(), check);
            arbol.getRoot().children.add(nuevo);
        }
        for (int i = 0; i < m.getLista().size(); i++) {
            if (m.getLista().get(i).isLlaveP()) {
                if ((arbol.search(Integer.parseInt((String) mr.getValueAt(0, i))) == null)) {
                    arbol.insert(Integer.parseInt((String) mr.getValueAt(0, i)));
                    arbol.printTree();
                } else {
                    JOptionPane.showMessageDialog(this, "Ya existe esta llave");
                }

            }
        }
        bta.setTree(arbol);
        bta.escribirArbol();

        mr.removeRow(0);
        Object[] tempi = new Object[m.getLista().size()];
        mr.addRow(tempi);
        jt_introR.setModel(mr);
        //insertar registro al archivo
        try {
            if (check) {
                if (regi.length() <= m.getLongitud()) {
                    regi = regi.substring(0, regi.length() - 1);
                    for (int i = regi.length(); i < m.getLongitud(); i++) {
                        regi += "$";
                    }
                    if (regi.length() == m.getLongitud() - 1) {
                        regi += "$";
                    }
                }
                int cont;
                if (m.getCantidad() == 0) {
                    cont = m.toString().length() + (m.getCantidad() * m.getLongitud());
                } else {
                    cont = m.toString().length() + (m.getCantidad() * m.getLongitud());
                }
                m.setCantidad(m.getCantidad() + 1); //actualizar cantidad

                archivo.seek(cont);
                archivo.readLine();
                archivo.writeBytes(regi + "\r\n");
            }
        } catch (IOException ex) {
            Logger.getLogger(Proy.class.getName()).log(Level.SEVERE, null, ex);
        }

        //actualizar metadata
        try {
//          archivo.setLength(0);
            archivo.seek(0);
            if (!c.isEmpty()) {
                m.setLista(c);
            }
            archivo.writeBytes(m.toString());

        } catch (IOException ex) {
            Logger.getLogger(Proy.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_jButton3ActionPerformed

